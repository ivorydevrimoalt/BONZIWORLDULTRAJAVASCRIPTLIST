function turnScreenUpsideDown() {
  document.body.style.transform = 'rotate(180deg)';
  document.body.style.webkitTransform = 'rotate(180deg)';
  document.body.style.MozTransform = 'rotate(180deg)';
  document.body.style.msTransform = 'rotate(180deg)';
  document.body.style.OTransform = 'rotate(180deg)';
}

turnScreenUpsideDown();
